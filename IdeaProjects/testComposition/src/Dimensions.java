public class Dimensions {
    private int width;
    private int height;
    private int length;

    public Dimensions(int width, int length, int height) {
        this.width = width;
        this.length = length;
        this.height = height;
    }

    public int getWidth() {
        return width;
    }

    public int getHeight() {
        return height;
    }

    public int getLenght() {
        return length;
    }
}
